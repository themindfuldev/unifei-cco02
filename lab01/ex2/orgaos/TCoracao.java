package orgaos;

public class TCoracao
{
	private int batimentoRepouso, batimentoEsforco, movMusculo, movValvulas, tamanho;
	
	public int getBatimentoRepouso()
	{
		return batimentoRepouso;
	}
	
	public void setBatimentoRepouso(int batimentoRepouso)
	{
		this.batimentoRepouso = batimentoRepouso;
	}
	
	public int getBatimentoEsforco()
	{
		return batimentoEsforco;
	}
	
	public void setBatimentoEsforco(int batimentoEsforco)
	{
		this.batimentoEsforco = batimentoEsforco;
	}
	
	public int getMovMusculo()
	{
		return movMusculo;
	}
	
	public void setMovMusculo(int movMusculo)
	{
		this.movMusculo = movMusculo;
	}
	
	public int getMovValvulas()
	{
		return movValvulas;
	}
	
	public void setMovValvulas(int movValvulas)
	{
		this.movValvulas = movValvulas;
	}
	
	public int getTamanho()
	{
		return tamanho;
	}
	
	public void setTamanho(int tamanho)
	{
		this.tamanho = tamanho;
	}				
}